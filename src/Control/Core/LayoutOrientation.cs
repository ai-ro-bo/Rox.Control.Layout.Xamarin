namespace Rox
{
    /// <summary>
    /// LayoutOrientation definition which represents which direction the layout should flow.
    /// </summary>
    public enum LayoutOrientation
    {
        /// <summary>
        /// The layout should flow in a horizontal manner.
        /// </summary>
        Horizontal = 1,

        /// <summary>
        /// The layout should flow in a vertical manner.
        /// </summary>
        Vertical = 2
    }
}